//concatenation
let result1 = 'hello'+ 'world';
console.log(result1);
let result2 = 'Aneri' .concat('here!');
console.log(result2);
console.log('\n');

//length
let length = 'hello'.length;
console.log(length);
console.log('\n');

//substring
let result3 = "Hello, World!".substring(8, 13);
console.log(result3); 
console.log('\n');

//slice
let result4 = "Hello, World!".slice(5, 13);
console.log(result4); 
console.log('\n');

//substr
let result5 = "Hello, World!".substr(9, 13);
console.log(result5); 
console.log('\n');

//find substring(indexoF)
let index = 'Thank you'.indexOf('you');
console.log(index);
console.log('\n');

//replace
let result6 = 'Thank You'.replace('you','God'); 
console.log(result6);
console.log('\n');

//uppercase
let result7 = "hello".toUpperCase();
console.log(result7);
console.log('\n');

//lowercase
let result8 = "HELLO".toLowerCase();
console.log(result8);
console.log('\n');

//trim spaces
let result9 = " Hello ".trim();
console.log(result9);
console.log('\n');